/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number}
 */
var searchInsert = function (nums, target) {
  let left = 0,
    right = nums.length - 1;

  while (left + 1 < right) {
    const mid = Math.floor((left + right) / 2);

    if (nums[mid] < target) {
      left = mid;
    } else {
      right = mid;
    }
  }

  if (nums[left] >= target) {
    return left;
  } else if (nums[right] >= target) {
    return right;
  } else if (nums[right] < target) {
    return right + 1;
  }

  return 0;
};
