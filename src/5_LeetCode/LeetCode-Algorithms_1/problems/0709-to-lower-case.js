// Implement function ToLowerCase() that has a string parameter str, and returns
// the same string in lowercase.

// Example 1:
// Input: "Hello"
// Output: "hello"
// Example 2:
// Input: "here"
// Output: "here"
// Example 3:
// Input: "LOVELY"
// Output: "lovely"


/**
 * @param {string} str
 * @return {string}
 */
const toLowerCase = function(str) {
  return str.toLowerCase
}

;[
  'Hello',
].forEach((str) => {
  console.log(toLowerCase(str))
})

